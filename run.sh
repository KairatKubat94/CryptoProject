#!/bin/bash

# Скрипт запуска визуализатора алгоритмов шифрования

echo "========================================"
echo "Визуализатор Алгоритмов Шифрования"
echo "========================================"
echo ""

# Проверяем Python
if ! command -v python3 &> /dev/null; then
    echo "❌ Python3 не установлен или не в PATH"
    echo "Пожалуйста установите Python 3.8+"
    exit 1
fi

echo "✓ Python найден: $(python3 --version)"

# Создаем виртуальное окружение
if [ ! -d "venv" ]; then
    echo ""
    echo "📦 Создание виртуального окружения..."
    python3 -m venv venv
    if [ $? -ne 0 ]; then
        echo "❌ Ошибка при создании виртуального окружения"
        exit 1
    fi
    echo "✓ Виртуальное окружение создано"
fi

# Активируем окружение
echo ""
echo "🔌 Активация виртуального окружения..."
source venv/bin/activate

# Устанавливаем зависимости
echo ""
echo "📥 Установка зависимостей..."
pip install -q -r requirements.txt
if [ $? -ne 0 ]; then
    echo "❌ Ошибка при установке зависимостей"
    exit 1
fi
echo "✓ Зависимости установлены"

# Запускаем приложение
echo ""
echo "🚀 Запуск приложения..."
echo ""
echo "========================================"
echo "✓ Сервер запущен!"
echo "========================================"
echo ""
echo "🌐 Откройте браузер и перейдите на:"
echo "   http://127.0.0.1:5000"
echo ""
echo "Нажмите Ctrl+C для остановки сервера"
echo "========================================"
echo ""

python3 app.py
